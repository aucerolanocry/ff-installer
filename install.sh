# ===== BARRA DE PROGRESSO VERMELHA PISCANTE =====
progress_bar() {
    progress=0
    while kill -0 $pid 2>/dev/null; do
        progress=$((progress+1))
        if [ $progress -gt 100 ]; then
            progress=100
        fi
        filled=$((progress/2))
        empty=$((50-filled))
        
        if ((progress % 2 == 0)); then
            color="\033[1;31m"
        else
            color="\033[0;31m"
        fi
        
        printf "\r${color}["
        printf "%0.sв–Ҳ" $(seq 1 $filled)
        printf "%0.sв–‘" $(seq 1 $empty)
        printf "] %3d%%\033[0m" "$progress"
        sleep 0.1
    done
}

